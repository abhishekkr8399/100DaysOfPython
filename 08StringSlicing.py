names="Abhishek Kumar,Amit Kumar"
print(len(names))     #length of names
print(names[0:14])    #Slicing[0:14)
print(names[:14])    #Same
print(names[4:14])    #Slicing[4:14)
print(names[:])        #print full string
print(names[0:-4])   #Slicing[0:len(names)-4]
print(names[-3:-1]) #Slicing[len(names)-3:len(names)-1] or[22:24]
